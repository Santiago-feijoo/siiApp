package com.example.siiapp;

public class Colaborador {

    private String codigoC, cedulaC, nombreC, estado, cargoC, turno, turnoBurbuja, grupo, jornada, ubicacion, subordinacion, Empresa, observacion, proceso;

    public String getCodigoC() {
        return codigoC;
    }

    public void setCodigoC(String codigoC) {
        this.codigoC = codigoC;
    }

    public String getCedulaC() {
        return cedulaC;
    }

    public void setCedulaC(String cedulaC) {
        this.cedulaC = cedulaC;
    }

    public String getNombreC() {
        return nombreC;
    }

    public void setNombreC(String nombreC) {
        this.nombreC = nombreC;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getCargoC() {
        return cargoC;
    }

    public void setCargoC(String cargoC) {
        this.cargoC = cargoC;
    }

    public String getTurno() {
        return turno;
    }

    public void setTurno(String turno) {
        this.turno = turno;
    }

    public String getTurnoBurbuja() {
        return turnoBurbuja;
    }

    public void setTurnoBurbuja(String turnoBurbuja) {
        this.turnoBurbuja = turnoBurbuja;
    }

    public String getGrupo() {
        return grupo;
    }

    public void setGrupo(String grupo) {
        this.grupo = grupo;
    }

    public String getUbicacion() {
        return ubicacion;
    }

    public String getJornada() {
        return jornada;
    }

    public void setJornada(String jornada) {
        this.jornada = jornada;
    }

    public void setUbicacion(String ubicacion) {
        this.ubicacion = ubicacion;
    }

    public String getSubordinacion() {
        return subordinacion;
    }

    public void setSubordinacion(String subordinacion) {
        this.subordinacion = subordinacion;
    }

    public String getEmpresa() {
        return Empresa;
    }

    public void setEmpresa(String empresa) {
        Empresa = empresa;
    }

    public String getObservacion() {
        return observacion;
    }

    public void setObservacion(String observacion) {
        this.observacion = observacion;
    }

    public String getProceso() {
        return proceso;
    }

    public void setProceso(String proceso) {
        this.proceso = proceso;
    }
}
